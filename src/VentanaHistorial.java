	private void cambiarDisponibilidad()
	{

		Connection c = conexionBD();
		String insert = "update Habitaciones set disponibilidad=? where numero=" + tblHistorialReservación.getValueAt( tblHistorialReservación.getSelectedRow() , 6 ).toString();

		try
		{
			Statement stmt = c.createStatement();
			PreparedStatement ps;

			ps = c.prepareStatement( insert );
			ps.setString( 1 , "Disponible" );

			ps.executeUpdate();

			c.close();
		}
		catch ( SQLException ex )
		{
			Logger.getLogger( VentanaReservacion.class.getName() ).log( Level.SEVERE , null , ex );

		}

	}

