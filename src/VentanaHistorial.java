	private void cargarTablaDesdeBD()
	{
		String q = "select * from Reservaciones;";

		Connection c = conexionBD();

		String prueba = "";

		int folio;

		String nombre;
		int edad;
		String telefono;
		String fechaEntrada;
		String fechaSalida;
		int numHabitacion;
		int total;

		try
		{

			Statement stmt = c.createStatement();
			stmt.execute( q );
			ResultSet rs = stmt.getResultSet();
			if ( rs != null )
			{
				while ( rs.next() )
				{
					folio = rs.getInt( "folioReserva" );
					nombre = rs.getString( "nombre" );
					edad = rs.getInt( "edad" );
					telefono = rs.getString( "telefono" );
					fechaEntrada = rs.getString( "fechaEntrada" );
					fechaSalida = rs.getString( "fechaSalida" );
					numHabitacion = rs.getInt( "numeroHabitacion" );
					total = rs.getInt( "costoTotal" );

					/*prueba += "(" + strNumero + "," + strTipo + "," + strDisponibilidad + ","
						+ strPrecio + ")\n";*/
					agregar_a_tabla( folio , nombre , edad , telefono , fechaEntrada , fechaSalida , numHabitacion , total );
				}
			}

			c.close(); // no dejar la conexión abierta

		}
		catch ( SQLException e )
		{
			System.out.println( "Error al cargar datos de la tabla Reservaciones" );
			System.out.println( e.getMessage() );
		}

		System.out.println( q + "\n" + prueba );

	}

