	public void editar()
	{
		String nombre = txtNombre.getText();
		String apellidoPaterno = txtAPaterno.getText();
		String apellidoMaterno = txtAMaterno.getText();
		int edad = Integer.parseInt( txtEdad.getText() );
		String domicilio = txtDomicilio.getText();
		String telefono = txtTelefono.getText();
		String cargo = txtCargo.getText();
		String horarioEntrada = txtHorario.getText();
		String horarioSalida = txtHorarioSalida.getText();

		Connection c = conexionBD();
		String insert = "update Empleado set nombre=?,apellidoPaterno=?,apellidoMaterno=?,edad=?,domicilio=?,telefono=?,cargo=?,horarioEntrada=?,horarioSalida=? where id=" + tblEmpleado.getValueAt( tblEmpleado.getSelectedRow() , 0 ).toString();

		try
		{
			Statement stmt = c.createStatement();
			PreparedStatement ps;

			ps = c.prepareStatement( insert );
			ps.setString( 1 , nombre );
			ps.setString( 2 , apellidoPaterno );
			ps.setString( 3 , apellidoMaterno );
			ps.setInt( 4 , edad );
			ps.setString( 5 , domicilio );
			ps.setString( 6 , telefono );
			ps.setString( 7 , cargo );
			ps.setString( 8 , horarioEntrada );
			ps.setString( 9 , horarioSalida );

			ps.executeUpdate();

			c.close();
		}
		catch ( SQLException ex )
		{
			Logger.getLogger( VentanaReservacion.class.getName() ).log( Level.SEVERE , null , ex );

		}

	}

