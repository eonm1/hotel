    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
		limpiar();
		bloquear();
		btnNuevo.setEnabled( true );
		btnAgregar.setEnabled( false );
		btnEditar.setEnabled( false );
		btnEliminar.setEnabled( false );
		m.setRowCount( 0 );
		cargarTablaDesdeBD();
    }//GEN-LAST:event_btnCancelarActionPerformed

	private void llenarTabla()
	{
		Object O[] = new Object[ 5 ];
		O[ 0 ] = txtNombre.getText();
		O[ 1 ] = txtAPaterno.getText();
		O[ 2 ] = txtAMaterno.getText();
		O[ 3 ] = txtCargo.getText();
		O[ 4 ] = txtHorario.getText();

		m.addRow( O );
	}
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

		if ( btnNuevo.isEnabled() == false && btnEditar.isEnabled() == true )

		{
			try
			{
				valida();
				validaEdad();
				validaTel();
				editar();
				m.setRowCount( 0 );
				cargarTablaDesdeBD();

				limpiar();
				bloquear();

				btnNuevo.setEnabled( true );
				btnAgregar.setEnabled( false );
				btnEliminar.setEnabled( false );
				btnEditar.setEnabled( false );

			}
			catch ( EmpleadoException e )
			{
				showMessageDialog( rootPane , e.getMessage() );
			}
		}
		else
		{
			try
			{
				valida();
				validaEdad();
				validaTel();
				insertar();
				m.setRowCount( 0 );
				cargarTablaDesdeBD();

				limpiar();
				bloquear();

				btnNuevo.setEnabled( true );
				btnAgregar.setEnabled( false );

			}
			catch ( EmpleadoException e )
			{
				showMessageDialog( rootPane , e.getMessage() );
			}
		}
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
		char e = evt.getKeyChar();
		if ( ( ( e < 'A' ) || ( e > 'Z' ) ) && ( ( e < 'a' ) || ( e > 'z' ) ) && ( e != ' ' ) )
		{
			evt.consume();
		}
    }//GEN-LAST:event_txtNombreKeyTyped

