	private void buscar( String cad )
	{
		String q = "select * from Empleado where id=" + cad + ";";

		Connection c = conexionBD();

		String prueba = "";

		int Id;
		String nombre;
		String apellidoPaterno;
		String apellidoMaterno;
		int edad;
		String domicilio;
		String telefono;
		String cargo;
		String horarioEntrada;
		String horarioSalida;

		try
		{

			Statement stmt = c.createStatement();
			stmt.execute( q );
			ResultSet rs = stmt.getResultSet();
			if ( rs != null )
			{
				while ( rs.next() )
				{
					Id = rs.getInt( "id" );
					nombre = rs.getString( "nombre" );
					apellidoPaterno = rs.getString( "apellidoPaterno" );
					apellidoMaterno = rs.getString( "apellidoMaterno" );
					edad = rs.getInt( "edad" );
					domicilio = rs.getString( "domicilio" );
					telefono = rs.getString( "telefono" );
					cargo = rs.getString( "cargo" );
					horarioEntrada = rs.getString( "horarioEntrada" );
					horarioSalida = rs.getString( "horarioSalida" );

					/*prueba += "(" + strNumero + "," + strTipo + "," + strDisponibilidad + ","
						+ strPrecio + ")\n";*/
					agregar_a_tabla( Id , nombre , apellidoPaterno , apellidoMaterno , edad , domicilio , telefono , cargo , horarioEntrada , horarioSalida );
				}
			}

			c.close(); // no dejar la conexión abierta

		}
		catch ( SQLException e )
		{
			System.out.println( "Error al cargar datos de la tabla " + "Hotel" );
			System.out.println( e.getMessage() );
		}

		System.out.println( q + "\n" + prueba );

	}

