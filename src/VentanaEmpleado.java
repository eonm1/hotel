	private void valida() throws EmpleadoException
	{
		if ( txtNombre.getText().equals( "" ) )
		{
			txtNombre.requestFocus();
			throw new EmpleadoException( "Casilla Nombre sin llenar" );
		}
		if ( txtAPaterno.getText().equals( "" ) )
		{
			txtAPaterno.requestFocus();
			throw new EmpleadoException( "Casilla Apellido Paterno sin llenar" );
		}
		if ( txtAMaterno.getText().equals( "" ) )
		{
			txtAMaterno.requestFocus();
			throw new EmpleadoException( "Casilla Apellido Materno sin llenar" );
		}
		if ( txtEdad.getText().equals( "" ) )
		{
			txtEdad.requestFocus();
			throw new EmpleadoException( "Casilla Edad sin llenar" );
		}
		if ( txtDomicilio.getText().equals( "" ) )
		{
			txtDomicilio.requestFocus();
			throw new EmpleadoException( "Casilla Domicilio sin llenar" );
		}
		if ( txtTelefono.getText().equals( "" ) )
		{
			txtTelefono.requestFocus();
			throw new EmpleadoException( "Casilla Telefono sin llenar" );
		}
		if ( txtCargo.getText().equals( "" ) )
		{
			txtCargo.requestFocus();
			throw new EmpleadoException( "Casilla Cargo sin llenar" );
		}

	}

	private void validaEdad() throws EmpleadoException
	{
		int edad = Integer.parseInt( txtEdad.getText() );
		if ( edad < 18 || edad > 100 )
		{
			txtEdad.requestFocus();
			throw new EmpleadoException( "Edad No Valida" );
		}

	}

	private void validaTel() throws EmpleadoException
	{
		String tel = txtTelefono.getText();
		int a = tel.length();
		if ( a != 10 )
		{
			txtTelefono.requestFocus();
			throw new EmpleadoException( "Telefono No Valida (10 digitos)" );
		}
	}

